#!/usr/bin/env node

/**
 * Compiles the gematria plugin TypeScript and packages the extension
 * into a zip ready for Chrome side-loading.
 */

import { execSync } from 'child_process'
import { createHash } from 'crypto'
import { mkdirSync, readFileSync, writeFileSync } from 'fs'
import { resolve, dirname } from 'path'
import { fileURLToPath } from 'url'

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const PLUGIN_DIR = resolve(ROOT, 'gematria/plugin')
const OUT_DIR = resolve(ROOT, 'public/downloads')
const ZIP_NAME = 'ccru-gematria-plugin.zip'
const ZIP_INFO_PATH = resolve(ROOT, 'app/gematria/plugin/zipInfo.ts')

// 1. Compile TypeScript
console.log('[plugin-zip] Compiling TypeScript…')
execSync('npx tsc --project gematria/plugin/tsconfig.json', {
  cwd: ROOT,
  stdio: 'inherit',
})

// 2. Ensure output directory exists
mkdirSync(OUT_DIR, { recursive: true })

// 3. Create zip preserving directory structure — only runtime files
console.log('[plugin-zip] Creating zip…')
const includes = [
  'manifest.json',
  'icons/icon-16.png',
  'icons/icon-32.png',
  'icons/icon-48.png',
  'icons/icon-128.png',
  'dist/src/background.js',
  'dist/src/ciphers.js',
  'dist/src/gematria.js',
  'dist/src/cyber-ui.js',
  'dist/src/content.js',
  'dist/popup/popup.js',
  'popup/popup.html',
  'popup/popup.css',
  'src/cyber-ui.css',
]

execSync(
  `zip -r9 "${OUT_DIR}/${ZIP_NAME}" ${includes.map(f => `"${f}"`).join(' ')}`,
  { cwd: PLUGIN_DIR, stdio: 'inherit' },
)

// 4. Compute SHA-256 and publish metadata for the plugin download page.
const zipPath = resolve(OUT_DIR, ZIP_NAME)
const zipSha256 = createHash('sha256').update(readFileSync(zipPath)).digest('hex')
const manifest = JSON.parse(readFileSync(resolve(PLUGIN_DIR, 'manifest.json'), 'utf8'))
const pluginVersion = typeof manifest.version === 'string' ? manifest.version : '0.1.0'

const zipInfoSource = [
  '// Auto-generated by scripts/build-plugin-zip.mjs',
  `export const PLUGIN_VERSION = '${pluginVersion}'`,
  `export const ZIP_PATH = '/downloads/${ZIP_NAME}'`,
  `export const ZIP_SHA256 = '${zipSha256}'`,
  '',
].join('\n')

writeFileSync(ZIP_INFO_PATH, zipInfoSource, 'utf8')

console.log(`[plugin-zip] SHA-256 ${zipSha256}`)
console.log('[plugin-zip] Updated app/gematria/plugin/zipInfo.ts')
console.log(`[plugin-zip] Done → public/downloads/${ZIP_NAME}`)
